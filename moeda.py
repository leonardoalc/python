def dobro(n):
    n *= 2
    nfor = f"{n:.2f}"
    float(nfor)
    return nfor

def diminuir(n):
    n10 = n * 0.1
    n -= n10
    nfor = f"{n:.2f}"
    float(nfor)
    return nfor

def metade(n):
    n /= 2
    nfor = f"{n:.2f}"
    float(nfor)
    return nfor

def aumentar(n):
    n10 = n * 0.1
    n += n10
    nfor = f"{n:.2f}"
    float(nfor)
    return nfor
